$(document).ready(function() {
    let dropdowns = $(".dropdown select");
    let btn = $("#btn");

    let fromCurr = $(".from select");
    let toCurr = $(".to select");

    let msg = $(".msg");

    dropdowns.each(function() {
        for (let currCode in countryList) {
            let newOption = $("<option>");
            newOption.text(currCode);
            newOption.val(currCode);

            if ($(this).attr("name") === "from" && currCode === "NPR") {
                newOption.prop("selected", true);
            } else if ($(this).attr("name") === "to" && currCode === "INR") {
                newOption.prop("selected", true);
            }

            $(this).append(newOption);
        }

        $(this).on("change", function() {
            updateFlag($(this));
        });
    });

    const updateFlag = (element) => {
        let currCode = element.val();
        let countryCode = countryList[currCode];
        let newSrc = `https://flagsapi.com/${countryCode}/flat/64.png`;
        let img = element.parent().find("img");
        img.attr("src", newSrc);
    };

    btn.on("click", async function(evt) {
        evt.preventDefault();
        let amount = $(".amount input");
        let amtVal = amount.val();

        if (amtVal === "" || amtVal < 1) {
            amtVal = 1;
            amount.val("1");
        }

        const URL = `${BASE_URL}/${fromCurr.val().toLowerCase()}/${toCurr.val().toLowerCase()}.json`;
        try {
            let response = await fetch(URL);
            let data = await response.json();
            let rate = data[toCurr.val().toLowerCase()];
            let finalAmount = amtVal * rate;
            msg.text(`${amtVal} ${fromCurr.val()} = ${finalAmount} ${toCurr.val()}`);
        } catch (error) {
            console.error("Error fetching data:", error);
            msg.text("Error fetching data. Please try again later.");
        }
    });
});
